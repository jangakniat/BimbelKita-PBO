    private void tabel_siswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_siswaMouseClicked
        label_photo.setText("                 Pilih Foto");
        label_photo.setIcon(null);
        txt_namaPhoto.setText(null);
        try {
            int row = tabel_siswa.rowAtPoint(evt.getPoint());
            String id = tabel_siswa.getValueAt(row, 0).toString();
            String nis = tabel_siswa.getValueAt(row, 1).toString();
            String nama = tabel_siswa.getValueAt(row, 2).toString();
            String kelas = tabel_siswa.getValueAt(row, 3).toString();
            String kelamin = tabel_siswa.getValueAt(row, 4).toString();
            String alamat = tabel_siswa.getValueAt(row, 5).toString();
            Date tgl = null;
            String tanggal_lahir = tabel_siswa.getValueAt(row, 6).toString();
            tgl = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(tanggal_lahir);
            hasil = crud.fotoSiswa(id);
            while (hasil.next()) {
                crud.setFoto(hasil.getString("foto"));
            }
            String foto = crud.getFoto();
            String path = new File(".").getCanonicalPath();
            System.out.println(path);
            System.out.println(foto);
            BufferedImage img = null;
            img = ImageIO.read(new File(path + "/img/siswa/" + foto));
            System.out.println(img);
            Image imagedResized = img.getScaledInstance(label_photo.getWidth(), label_photo.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon imageIcon = new ImageIcon(imagedResized);
            label_photo.setText(null);
            label_photo.setIcon(imageIcon);
            txt_id.setText(String.valueOf(id));
            txt_nis.setText(String.valueOf(nis));
            txt_nama.setText(String.valueOf(nama));
            combobox_kelas.setSelectedItem(kelas);
            txt_namaPhoto.setText(foto);
            txt_namaPhoto.show();
            if ("L".equals(kelamin)) {
                radio_L.setSelected(true);
            } else if ("P".equals(kelamin)) {
                radio_P.setSelected(true);
            }
            txt_alamat.setText(String.valueOf(alamat));
            txt_tanggal.setDate(tgl);

        } catch (Exception e) {
            System.out.println(e);

        }
    }//GEN-LAST:event_tabel_siswaMouseClicked

