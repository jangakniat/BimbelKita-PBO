    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        if (txt_nis.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, nis belum diisi !");
            txt_id.requestFocus();
        } else if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, nama belum diisi !");
            txt_id.requestFocus();
        } else if (buttonGroup1.getSelection() == null) {
            JOptionPane.showMessageDialog(null, "Maaf, kelamin belum diisi !");
            txt_id.requestFocus();
        } else if (txt_alamat.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, alamat belum diisi !");
            txt_id.requestFocus();
        } else if (txt_tanggal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Maaf, tanggal lahir belum diisi !");
            txt_id.requestFocus();
        } else {
            try {
                int id = Integer.parseInt(txt_id.getText());
                int nis = Integer.parseInt(txt_nis.getText());
                String nama = txt_nama.getText();
                int kelas = Integer.parseInt(combobox_kelas.getSelectedItem().toString());
                String kelamin = null;

                if (radio_L.isSelected()) {
                    kelamin = "L";
                } else if (radio_P.isSelected()) {
                    kelamin = "P";
                }
                String alamat = txt_alamat.getText();
                SimpleDateFormat tgl = new SimpleDateFormat("yyyy-MM-dd");
                String tanggal_lahir = String.valueOf(tgl.format(txt_tanggal.getDate()));
                String filename = file.toString();
                String extensi = FilenameUtils.getExtension(filename);
                String foto = UUID.randomUUID().toString() + "." + extensi;
                hasil = crud.fotoSiswa(txt_id.getText());
                while (hasil.next()) {
                    crud.setFoto(hasil.getString("foto"));
                }
                String oldFoto = crud.getFoto();

                crud.setNama(nama);
                crud.setNis(nis);
                crud.setKelas(kelas);
                crud.setAlamat(alamat);
                crud.setKelamin(kelamin);
                crud.ubahData(id, nis, nama, kelas, kelamin, alamat, tanggal_lahir, foto);

                try {
                    String path = new File(".").getCanonicalPath();
                    String movePath = path + "/img/siswa/" + foto;
                    FileUtils.copyFile(
                            FileUtils.getFile(file),
                            FileUtils.getFile(movePath));
                    FileUtils.deleteQuietly(new File(path + "/img/siswa/" + oldFoto));

                } catch (IOException ex) {
                    Logger.getLogger(Siswa_Form.class.getName()).log(Level.SEVERE, null, ex);
                }
                reset_text();
                tampil_database();
            } catch (NumberFormatException e) {
                System.out.println(e);
            } catch (SQLException ex) {
                Logger.getLogger(Siswa_Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

