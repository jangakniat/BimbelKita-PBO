    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        Object[] baris = {"ID", "NIS", "Nama", "kelas", "kelamin", "Alamat", "Tanggal Lahir"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_siswa.setModel(tabmode);
        try {
            crud.setKeyword(txt_cari.getText());
            hasil = crud.searchData(crud.getKeyword());

            while (hasil.next()) {
                crud.setID(hasil.getString("siswa_id"));
                crud.setNis(hasil.getInt("nis"));
                crud.setNama(hasil.getString("nama"));
                crud.setKelas(hasil.getInt("kelas"));
                crud.setKelamin(hasil.getString("kelamin"));
                crud.setAlamat(hasil.getString("alamat"));
                crud.setTanggal_Lahir(hasil.getDate("tanggal_lahir"));
                String tgl = crud.getTanggal_Lahir().toString();
                String nis = String.valueOf(crud.getNis());
                String kelas = String.valueOf(crud.getKelas());
                String[] data = {crud.getID(), nis, crud.getNama(), kelas, crud.getKelamin(), crud.getAlamat(), tgl};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_btn_cariActionPerformed

