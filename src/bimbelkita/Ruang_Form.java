    private void tabel_ruangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_ruangMouseClicked
        try {
            int row = tabel_ruang.rowAtPoint(evt.getPoint());
            String id = tabel_ruang.getValueAt(row, 0).toString();
            String nama = tabel_ruang.getValueAt(row, 1).toString();
            txt_id.setText(id);
            txt_nama.setText(nama);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabel_ruangMouseClicked

    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        if (txt_nama.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Maaf, nama belum diisi !");
            txt_id.requestFocus();
        } else {
            try {
                String nama = txt_nama.getText();

                crud.setNama(nama);
                crud.simpanData(crud.getNama());
                reset_text();
                tampilRuang();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btn_insertActionPerformed

