    public void tampilRuang() {
        Object[] baris = {"ID", "Nama"};
        tabmode = new DefaultTableModel(null, baris);
        tabel_ruang.setModel(tabmode);
        try {

            hasil = crud.tampilRuang();
            while (hasil.next()) {
                crud.setId(hasil.getString("id"));
                crud.setNama(hasil.getString("nama"));
                String[] data = {crud.getId(), crud.getNama()};
                tabmode.addRow(data);
            }
        } catch (SQLException e) {
            System.out.println("e" + e);
        }
    }

